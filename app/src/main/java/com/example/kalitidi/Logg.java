package com.example.kalitidi;

public class Logg {
    public String Id;
    public String Logg;


    public Logg(String Id, String Logg) {
        this.Id = Id;
        this.Logg = Logg;
    }

    public String getId() {
        return Id;
    }

    public void setId(String Id) {
        Id = Id;
    }

    public String getLogg() {
        return Logg;
    }

    public void setLogg(String Logg) {
        Logg = Logg;
    }


}
